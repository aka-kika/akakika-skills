#!/usr/bin/env node
/**
 * Scan a project directory for common cleanup candidates.
 *
 * Usage:
 *   node scan-cleanup-candidates.mjs <project-path> [--json]
 */

import { readdirSync, statSync, lstatSync } from "fs";
import { basename, join, relative, resolve } from "path";

const DEFAULT_TARGETS = [
  { pattern: "node_modules", type: "dir", category: "JavaScript deps", reason: "Reinstall with package manager" },
  { pattern: ".next", type: "dir", category: "Next.js build", reason: "Regenerated on build" },
  { pattern: "dist", type: "dir", category: "Build output", reason: "Regenerated on build" },
  { pattern: "build", type: "dir", category: "Build output", reason: "Regenerated on build" },
  { pattern: "out", type: "dir", category: "Build output", reason: "Regenerated on build" },
  { pattern: ".turbo", type: "dir", category: "Turborepo cache", reason: "Regenerated on run" },
  { pattern: ".parcel-cache", type: "dir", category: "Parcel cache", reason: "Regenerated on build" },
  { pattern: "coverage", type: "dir", category: "Test coverage", reason: "Regenerated by tests" },
  { pattern: ".pytest_cache", type: "dir", category: "Python test cache", reason: "Regenerated by pytest" },
  { pattern: "__pycache__", type: "dir", category: "Python bytecode", reason: "Regenerated on import" },
  { pattern: ".mypy_cache", type: "dir", category: "Python linter cache", reason: "Regenerated by mypy" },
  { pattern: ".ruff_cache", type: "dir", category: "Python linter cache", reason: "Regenerated by ruff" },
  { pattern: ".cache", type: "dir", category: "Generic cache", reason: "Tool cache" },
  { pattern: ".vite", type: "dir", category: "Vite cache", reason: "Regenerated on dev" },
  { pattern: "target", type: "dir", category: "Rust/Cargo build", reason: "cargo build" },
  { pattern: ".DS_Store", type: "file", category: "macOS metadata", reason: "Safe to remove" },
  { pattern: "Thumbs.db", type: "file", category: "Windows metadata", reason: "Safe to remove" },
];

const NEVER_DELETE = new Set([".git", ".env", "src", "app", "lib", "packages", "README.md"]);

function formatBytes(bytes) {
  if (bytes === 0) return "0 B";
  const units = ["B", "KB", "MB", "GB"];
  const i = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), units.length - 1);
  return `${(bytes / 1024 ** i).toFixed(i === 0 ? 0 : 1)} ${units[i]}`;
}

function dirSize(targetPath) {
  let total = 0;
  const stack = [targetPath];
  while (stack.length) {
    const current = stack.pop();
    let entries;
    try {
      entries = readdirSync(current, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const entry of entries) {
      const full = join(current, entry.name);
      try {
        if (entry.isDirectory()) {
          stack.push(full);
        } else if (entry.isFile() || entry.isSymbolicLink()) {
          total += lstatSync(full).size;
        }
      } catch {
        // skip unreadable
      }
    }
  }
  return total;
}

function pathSize(targetPath) {
  try {
    const st = lstatSync(targetPath);
    if (st.isDirectory()) return dirSize(targetPath);
    return st.size;
  } catch {
    return 0;
  }
}

function findNamedTargets(projectRoot, definitions) {
  const found = [];
  const queue = [projectRoot];

  while (queue.length) {
    const current = queue.pop();
    let entries;
    try {
      entries = readdirSync(current, { withFileTypes: true });
    } catch {
      continue;
    }

    for (const entry of entries) {
      if (NEVER_DELETE.has(entry.name) && current === projectRoot) continue;

      const full = join(current, entry.name);
      const rel = relative(projectRoot, full).split("\\").join("/");

      for (const def of definitions) {
        const nameMatch = entry.name === def.pattern;
        const suffixMatch = def.pattern.startsWith("*.") && entry.name.endsWith(def.pattern.slice(1));
        if (!nameMatch && !suffixMatch) continue;
        if (def.type === "dir" && !entry.isDirectory()) continue;
        if (def.type === "file" && entry.isDirectory()) continue;

        found.push({
          path: rel,
          absolutePath: full,
          category: def.category,
          reason: def.reason,
          bytes: pathSize(full),
        });
      }

      if (entry.isDirectory() && entry.name !== "node_modules" && !entry.name.startsWith(".")) {
        queue.push(full);
      } else if (entry.isDirectory()) {
        queue.push(full);
      }
    }
  }

  const byPath = new Map();
  for (const item of found) {
    const existing = byPath.get(item.path);
    if (!existing || item.bytes > existing.bytes) byPath.set(item.path, item);
  }

  return [...byPath.values()].sort((a, b) => a.path.localeCompare(b.path));
}

function main() {
  const args = process.argv.slice(2);
  const json = args.includes("--json");
  const projectPath = resolve(args.find((a) => !a.startsWith("-")) || ".");

  let stat;
  try {
    stat = statSync(projectPath);
  } catch {
    console.error(`Project path not found: ${projectPath}`);
    process.exit(1);
  }
  if (!stat.isDirectory()) {
    console.error(`Not a directory: ${projectPath}`);
    process.exit(1);
  }

  const candidates = findNamedTargets(projectPath, DEFAULT_TARGETS);
  const totalBytes = candidates.reduce((sum, c) => sum + c.bytes, 0);

  const result = {
    projectPath,
    projectName: basename(projectPath),
    candidateCount: candidates.length,
    totalBytes,
    totalHuman: formatBytes(totalBytes),
    candidates: candidates.map(({ path, category, reason, bytes }) => ({
      path,
      category,
      reason,
      bytes,
      human: formatBytes(bytes),
    })),
  };

  if (json) {
    console.log(JSON.stringify(result, null, 2));
    return;
  }

  console.log(`Project: ${result.projectName}`);
  console.log(`Path: ${projectPath}`);
  console.log(`Candidates: ${result.candidateCount} (${result.totalHuman} reclaimable)\n`);

  if (candidates.length === 0) {
    console.log("No default cleanup candidates found.");
    return;
  }

  for (const c of result.candidates) {
    console.log(`  ${c.human.padStart(8)}  ${c.path}  [${c.category}]`);
  }
}

main();
